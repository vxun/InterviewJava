
public class ShiftingOperation {
    public static void main(String[] args) {
        System.out.println("32 >> 32 = "+ (32 >> 32));
        System.out.println("32 >> 24 = "+ (32 >> 24));
        System.out.println("32 >> 16 = "+ (32 >> 16));
        System.out.println("32 >> 8 = "+ (32 >> 8));
        System.out.println("32 >> 4 = "+ (32 >> 4));
    }
}